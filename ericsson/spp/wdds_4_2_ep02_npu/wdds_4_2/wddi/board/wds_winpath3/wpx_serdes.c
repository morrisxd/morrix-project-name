WP_boolean WPX_SerdesUsedByPCIe(WP_U32 wpid, WP_U32 serdes)
{
   wpx_serdes_mux_ctrl data;
   wpx_serdes_mux_ctrl *serdes_mux_reg;
   wpx_serdes *serdes_reg;

   serdes_reg = WPX_SerdesBaseGet(wpid);
   serdes_mux_reg = &serdes_reg->serdes_mux.mux_ctrl;

   WPI_MEM_GET(data.mux_ctrl0, serdes_mux_reg->mux_ctrl0);
   WPI_MEM_GET(data.mux_ctrl1, serdes_mux_reg->mux_ctrl1);

   if(serdes == WPX_SERDES0)
   {
      if((data.mux_ctrl0 & SERDES_MUX_CTRL0_SERDES0_MASK) == SERDES_MUX_CTRL0_SERDES0_PCIE)
         return WP_TRUE;
   }
   else if(serdes == WPX_SERDES1)
   {
      if((data.mux_ctrl0 & SERDES_MUX_CTRL0_SERDES1_MASK) == SERDES_MUX_CTRL0_SERDES1_PCIE)
         return WP_TRUE;
   }
   return WP_FALSE;
}

