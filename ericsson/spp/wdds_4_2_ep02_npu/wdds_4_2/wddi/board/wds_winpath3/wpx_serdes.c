WP_status WPX_BoardSerdesInit( WP_U32 wpid,
                               WP_port port,
                               WP_U32 loopback_mode)
{
   WP_status status;
   WPX_SERDES_LANES serdes_index;
   WP_U32 lane_index;
   WP_U32 rate;
   wpx_port_to_serdes_connection *serdes_data;
   wpx_serdes_lane_data *serdes_lane_data;

   if((port < WP_PORT_ENET1) || (port > WP_PORT_XGI2))
      return WPI_ERROR(WP_ERR_CONFIG);

   serdes_data =  &(port_to_serdes[current_configuration]);
   serdes_lane_data = &(serdes_data->lane_data[port-WP_PORT_ENET1]);

   serdes_index = serdes_lane_data->serdes_index;
   lane_index = serdes_lane_data->lane_index;
   rate = serdes_lane_data->rate;

   if(serdes_index == WPX_SERDES_NA ||
      lane_index == WPX_SERDES_LANE_NA ||
      rate == WPX_SERDES_RATE_NA)
      return WPI_ERROR(WP_ERR_CONFIG);

   if(lane_index != WPX_SERDES_LANE_ALL)
   {
      if(loopback_mode == WPX_SERDES_LOOPBACK)
      {
         serdes_loopback_mode[serdes_index] |= 1<<lane_index;
      }
      else
      {
         serdes_loopback_mode[serdes_index] &= ~(1<<lane_index);
      }
   }
   else
   {
      if(loopback_mode == WPX_SERDES_LOOPBACK)
      {
         serdes_loopback_mode[serdes_index] |= 0xf;
      }
      else
      {
         serdes_loopback_mode[serdes_index] &= ~(0xf);
      }
   }

   if (serdes_index >= WPX_SERDES_SINGLE_LANE_FIRST && serdes_index <= WPX_SERDES_SINGLE_LANE_LAST)
   {
      WPX_SerdesSetSingleLane(wpid,
                              serdes_index,
                              rate,
                              serdes_loopback_mode[serdes_index]);
   }
   else if(serdes_index >= WPX_SERDES_QUAD_LANE_FIRST &&
           serdes_index <= WPX_SERDES_QUAD_LANE_LAST)
   {

	  WP_U32 isXgi = WPX_SERDES_NOT_XGI;

	  if (port == WP_PORT_XGI1 || port == WP_PORT_XGI2)
	     isXgi = WPX_SERDES_XGI;

      WPI_RETURN_IF_ERROR(status,
                          WPX_SerdesSetQuadLane(wpid,
                                                serdes_index,
                                                rate,
                                                serdes_loopback_mode[serdes_index],
                                                isXgi));
   }
   else
      return WPI_ERROR(WP_ERR_CONFIG);

   return WP_OK;
}


