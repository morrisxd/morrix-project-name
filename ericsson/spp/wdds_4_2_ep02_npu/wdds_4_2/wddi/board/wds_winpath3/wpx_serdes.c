wpx_serdes *WPX_SerdesBaseGet(WP_U32 wpid)
{
   return (wpx_serdes *)(WPX_AppWpIntRegBaseGet(wpid) + WPI_SERDES_BASE);
}


WP_status WPX_SerdesSetSingleLane(WP_U32 wpid, WP_U32 serdes, WP_U32 rate, WP_U8 loopback_mode)
{
   WP_status status = WP_OK;
   wpx_serdes *serdes_regs;
   wpx_serdes_mux *mux_regs;
   WP_U32 ii;
   WP_U32 polling_result;
   volatile wpx_serdes_single_io_ctrl *single_io_control;

   if(serdes > WPX_SERDES_SINGLE_LANE_LAST)
      return WPI_ERROR(WP_ERR_SERDES_ILLEGAL);

   /**********************************************************************************
   * if this serdes uses PCIe (for Multi Winpath), do not change its configurations 
   **********************************************************************************/
   if(WPX_SerdesUsedByPCIe(wpid, serdes))
      return status;

   serdes_regs = WPX_SerdesBaseGet(wpid);
   mux_regs = &(serdes_regs->serdes_mux);

   /*init sequence*/
   if(rate == WPX_SERDES_1G)
   {
      WPX_SerdesInit_1_G_SingleLane(serdes_regs, serdes, loopback_mode);
   }
   else if(rate == WPX_SERDES_2_5G)
   {
      WPX_SerdesInit_2_5_G_SingleLane(serdes_regs, serdes, loopback_mode);
   }
   else return WPI_ERROR(WP_ERR_SERDES_RATE);

   for(ii = 0; ii<WPX_SERDES_INIT_POLLING_LOOPS; ii++)
   {
      WP_Delay(WPX_SERDES_INIT_DELAY);
      single_io_control= &(mux_regs->io_ctrl_single[serdes]);

      WPI_MEM_GET(polling_result, single_io_control->single_io_ctrl1);
      if(WPX_SERDES_SINGLE_POLLING_MASK & polling_result)
         break;
   }
   if(ii ==  WPX_SERDES_INIT_POLLING_LOOPS)
      return WPI_ERROR(WP_ERR_SERDES_INIT);

   return status;
}

WP_status WPX_SerdesSetQuadLane(WP_U32 wpid, WP_U32 serdes, WP_U32 rate, WP_U8 loopback_mode, WP_U32 isXgi)
{
    WP_status status = WP_OK;

   wpx_serdes *serdes_regs;
   wpx_serdes_quad_io_ctrl *quad_io_ctrl;
   WP_U32 ii;
   WP_U32 polling_result;
   wpx_serdes_mux *mux_regs;

   if((serdes < WPX_SERDES_QUAD_LANE_FIRST) ||
      (serdes > WPX_SERDES_QUAD_LANE_LAST))
      return WPI_ERROR(WP_ERR_SERDES_ILLEGAL);

   serdes_regs = WPX_SerdesBaseGet(wpid);
   mux_regs = &(serdes_regs->serdes_mux);
   quad_io_ctrl = &(mux_regs->io_ctrl_quad[serdes-WPX_SERDES_QUAD_LANE_FIRST]);

   /*init sequence*/
   if(rate == WPX_SERDES_1G)
   {
      WPX_SerdesInit_1_G_QuadLane(serdes_regs, serdes, loopback_mode);
   }
   else if(rate == WPX_SERDES_2_5G)
   {
      WPX_SerdesInit_2_5_G_QuadLane(serdes_regs, serdes, loopback_mode,isXgi);
   }
   else return WPI_ERROR(WP_ERR_SERDES_RATE);

   for(ii = 0; ii<WPX_SERDES_INIT_POLLING_LOOPS; ii++)
   {
      WP_Delay(WPX_SERDES_INIT_DELAY);
      WPI_MEM_GET(polling_result, quad_io_ctrl->quad_io_stat);
      if(polling_result & WPX_SERDES_QUAD_POLLING_MASK)
         break;
   }
   if(ii ==  WPX_SERDES_INIT_POLLING_LOOPS)
      return WPI_ERROR(WP_ERR_SERDES_INIT);

    return status;
}


